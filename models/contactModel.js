const { Schema, model } = require("mongoose");

const contactModel = new Schema({
  name: {
    type: String,
    required: [true, "Set name for contact"],
  },
  email: {
    type: String,
  },
  phone: {
    type: String,
  },
  favorite: {
    type: Boolean,
    default: false,
  },
  owner: {
    type: Schema.Types.ObjectId,
    ref: "users",
  },
});

const Contact = model("contacts", contactModel);

module.exports = Contact;
